user_reply = input("Bạn có thích rô bốt không (Hãy điền có, không, có thể, hoặc chỉ thích những con có mắt laze)")
if user_reply == "có":
    print("Bíp bíp!")
elif user_reply == "có thể":
    print("Quyết đoán lên, đồ người.")
elif user_reply == "chỉ thích những con có mắt laze":
    print("Rẹẹẹẹt!")
elif user_reply == "không":
    print("Hừm, rô bốt cũng chả thích cậu lắm.")
else:
    print("Chả hiểu nói gì, loài người đã xúc phạm chúng ta!")                